														   <p style="font-size: 130%;font-style: initial;text-align:justify;font-weight: 600;color: black;">What is Ulcerative Colitis ?</p>
														   <p style="text-align:justify;">Ulcerative colitis is a chronic inflammatory bowel disease (IBD) that causes long-lasting inflammation and ulcers (sores) in your large intestine. Ulcerative colitis affects the innermost lining (mucosal lining) of the large intestine (colon and rectum). Treatment can greatly reduce signs and symptoms of the disease and bring about long term remission. However the patient in remission requires life long treatment and follow up. Ulcerative colitis symptoms can vary, depending on the severity and extent of inflammation. Therefore, doctors often classify ulcerative colitis according to its location/ extent. The patient may have the following signs and symptoms. Diarrhea (often with blood or pus) Rectal bleeding - passing blood with stool Abdominal pain and cramping, Rectal pain with urgency to defecate. Weight loss, fatigue, Fever, Some patients may have symptoms in other parts of the body apart from intestine like joint Pains, backache, inflammation of the eyes, mouth and skin ulcers and liver inflammation. Most people with ulcerative colitis have mild to moderate symptoms. The course of ulcerative colitis may vary, with some people having long periods of remission. However, some patients may have very severe course leading to need for surgery. Long-term disease may lead to development of colon cancer in some patients. Therefore, in these patients require periodic check up (surveillance)</p>
